// اختيار ذكر عشوائي
function getRandomZikr() {
  const randomIndex = Math.floor(Math.random() * azkar.length);
  return azkar[randomIndex];
}

// تحديث محتوى <p> داخل .zkr-box
document.addEventListener("DOMContentLoaded", function () {
  const zikrText = document.querySelector(".zkr-box p");
  if (zikrText) {
    zikrText.textContent = getRandomZikr();
  }
});
